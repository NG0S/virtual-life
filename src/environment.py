
class Tile:

    def __init__(self):
        self.neighbors_list = list()
        self.agent = None

    def add_neighbor(neighbor_tile):
        self.neighbors_list.append(neighbor_tile)

    def add_list_neighbors(list_neighbors):
        self.neighbors_list.extend(list_neighbors)

    def get_neighbors():
        return self.neighbors_list()

    def add_agent(agent):
        self.agent = agent

    def remove_agent():
        self.agent = None


